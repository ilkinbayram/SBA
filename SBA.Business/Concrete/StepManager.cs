        private readonly IStepDal _stepDal;
        private readonly IMapper _mapper;

        public StepManager(IStepDal stepDal,
                                IMapper mapper)
        {
            _stepDal = stepDal;
            _mapper = mapper;
        }

        public IDataResult<int> Add(Step stepModel)
        {
            try
            {
                int affectedRows = _stepDal.Add(stepModel);

                if (affectedRows <= 0)
                    throw new Exception(Messages.ErrorMessages.NOT_ADDED_AND_ROLLED_BACK);

                return new SuccessDataResult<int>(affectedRows, Messages.BusinessDataAdded);
            }
            catch (Exception exception)
            {
                return new ErrorDataResult<int>(-500, $"Exception Message: {$"Exception Message: {exception.Message} \nInner Exception: {exception.InnerException}"} \nInner Exception: {exception.InnerException}");
            }
        }

        public IDataResult<int> Remove(long Id)
        {
            try
            {
                IDataResult<int> dataResult;

                var deletableEntity = _stepDal.Get(x => x.Id == Id);

                if (deletableEntity == null)
                {
                    dataResult = new SuccessDataResult<int>(-1, Messages.DeletableDataWasNotFound);
                    return dataResult;
                }

                int affectedRows = _stepDal.Remove(deletableEntity);
                if (affectedRows > 0)
                {
                    dataResult = new SuccessDataResult<int>(affectedRows, Messages.BusinessDataDeleted);
                }
                else
                {
                    dataResult = new ErrorDataResult<int>(-1, false, Messages.BusinessDataWasNotDeleted);
                }

                return dataResult;
            }
            catch (Exception exception)
            {
                return new ErrorDataResult<int>(-1, true, $"Exception Message: {exception.Message} \nInner Exception: {exception.InnerException}");
            }
        }

        public IDataResult<Step> Get(Expression<Func<Step, bool>> filter)
        {
            try
            {
                var response = _stepDal.Get(filter);
                var mappingResult = _mapper.Map<Step>(response);
                return new SuccessDataResult<Step>(mappingResult);
            }
            catch (Exception exception)
            {
                return new ErrorDataResult<Step>(null, $"Exception Message: {exception.Message} \nInner Exception: {exception.InnerException}");
            }
        }

        public IDataResult<List<Step>> GetList(Expression<Func<Step, bool>> filter = null)
